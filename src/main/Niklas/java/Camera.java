public class Camera {

    private double height;
    private double radius = height;
    private double diameter = radius * 2;


    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getDiameter() {
        return diameter;
    }

    public double getRadius() {
        return radius;
    }




}
